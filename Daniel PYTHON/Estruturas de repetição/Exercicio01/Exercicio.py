# ENTRADA DE DADOS

quinze = 15

duzentos = 200

# PROCESSAMENTO / SAÍDA DE DADOS

while quinze <= duzentos:
    quadrado = quinze ** 2
    print('O quadrado de', quinze, 'é', quadrado)

    quinze += 1
